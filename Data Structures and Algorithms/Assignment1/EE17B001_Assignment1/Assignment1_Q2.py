
# The range constructor produces a range of values starting from the first parameter as the lower bound,
# the second parameter as the upper bound (NOT INCLUDED), and the third parameter as the step size.



# (a)

r1 = range(60,81,10)                # 81 is the excluded upper bound, and not 80, since we require the value of 80 as well
# print(list(r1))                   # Uncomment this line to check the list of the range produced


# (b)

r2 = range(4, -5, -2)               # -5 is the excluded upper bound, and not -4, since we require the value of -4 as well                       
